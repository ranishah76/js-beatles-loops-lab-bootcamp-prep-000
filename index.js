
function theBeatlesPlay (musicians, instruments) {
const variable = []

for (var i = 0; i < musicians.length; i++) {
    variable.push (musicians[i] + " plays " + instruments[i])
}
return variable
}

function johnLennonFacts (facts) {

  var i = 0;
  while (i < facts.length) {
    facts[i] += "!!!"
    i++;
}
  return facts
}

